/* ===========================================================================
 * CSUSB (CSE5250) HINT -- Part B (OPTIONAL / EXTRA CREDIT in this course)
 * ---------------------------------------------------------------------------
 * Part B extends ONLY the Sleeping thread pool to support asynchronous launches
 * with dependencies: runAsyncWithDeps(runnable, n, deps) returns a TaskID and
 * does NOT block; sync() blocks until everything finished.
 *
 * Suggested design:
 *   - Model each launch ("bulk task") as a node in a dependency graph.
 *   - Keep a "ready" queue (bulk tasks whose deps are all done) and a "waiting"
 *     set (still blocked). A bulk task becomes ready when its dep count hits 0.
 *   - Worker threads pull individual tasks from ready bulk tasks; when the last
 *     task of a bulk task finishes, decrement successors' dep counts and move
 *     any that reach 0 into the ready queue.
 *   - sync() waits until all bulk tasks are complete.
 *
 * If your instructor assigned Part A only, you can leave these as-is.
 * =========================================================================== */


